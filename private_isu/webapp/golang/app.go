func myMiddleware(next http.Handler) http.Handler {
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		log.Printf("%+v", chi.RouteContext(r.Context()))
		currRoutePattern := chi.RouteContext(r.Context()).RoutePattern()
		cookie, err := r.Cookie("my_session_id")
		sid := cookie.Value
		if err != nil {
			uuid, _ := uuid.NewRandom()
			http.SetCookie(w, &http.Cookie{
				Name:  "my_session_id",
				Value: fmt.Sprintf("%s", uuid),
			})
			sid = fmt.Sprintf("%s", uuid)
		} else {
			if prevRoutePattern, ok := userPrevActivity.Load(sid); ok {
				trans := fmt.Sprintf("%s --> %s", prevRoutePattern, currRoutePattern)
				cnt, _ := userActivity.LoadOrStore(trans, int64(0))
				userActivity.Store(trans, cnt.(int64)+1)
				log.Printf("%s: %d\n", trans, cnt.(int64)+1)
			}
		}
		userPrevActivity.Store(sid, currRoutePattern)
		next.ServeHTTP(w, r)
		log.Printf("%+v", chi.RouteContext(r.Context()))
	})
}

func main() {
	go standalone.Integrate(":8888")

	host := os.Getenv("ISUCONP_DB_HOST")
	if host == "" {
		host = "localhost"
	}
	port := os.Getenv("ISUCONP_DB_PORT")
	if port == "" {
		port = "3306"
	}
	_, err := strconv.Atoi(port)
	if err != nil {
		log.Fatalf("Failed to read DB port number from an environment variable ISUCONP_DB_PORT.\nError: %s", err.Error())
	}
	user := os.Getenv("ISUCONP_DB_USER")
	if user == "" {
		user = "root"
	}
	password := os.Getenv("ISUCONP_DB_PASSWORD")
	dbname := os.Getenv("ISUCONP_DB_NAME")
	if dbname == "" {
		dbname = "isuconp"
	}

	dsn := fmt.Sprintf(
		"%s:%s@tcp(%s:%s)/%s?charset=utf8mb4&parseTime=true&loc=Local",
		user,
		password,
		host,
		port,
		dbname,
	)

	db, err = sqlx.Open("mysql", dsn)
	if err != nil {
		log.Fatalf("Failed to connect to DB: %s.", err.Error())
	}
	defer db.Close()

	db.SetConnMaxLifetime(10 * time.Second)
	db.SetMaxIdleConns(512)
	db.SetMaxOpenConns(512)

	r := chi.NewRouter()

	r.Use(myMiddleware)

	r.Get("/initialize", getInitialize)
	r.Get("/login", getLogin)
	r.Post("/login", postLogin)
	r.Get("/register", getRegister)
	r.Post("/register", postRegister)
	r.Get("/logout", getLogout)
	r.Get("/", getIndex)
	r.Get("/posts", getPosts)
	r.Get("/posts/{id}", getPostsID)
	r.Post("/", postIndex)
	r.Get("/image/{id}.{ext}", getImage)
	r.Post("/comment", postComment)
	r.Get("/admin/banned", getAdminBanned)
	r.Post("/admin/banned", postAdminBanned)
	r.Get(`/@{accountName:[a-zA-Z]+}`, getAccountName)
	r.Get("/*", func(w http.ResponseWriter, r *http.Request) {
		http.FileServer(http.Dir("../public")).ServeHTTP(w, r)
	})

	log.Fatal(http.ListenAndServe(":8080", r))
}
