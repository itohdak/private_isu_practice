func postIndex(w http.ResponseWriter, r *http.Request) {
	me := getSessionUser(r)
	if !isLogin(me) {
		http.Redirect(w, r, "/login", http.StatusFound)
		return
	}

	if r.FormValue("csrf_token") != getCSRFToken(r) {
		w.WriteHeader(http.StatusUnprocessableEntity)
		return
	}

	file, header, err := r.FormFile("file")
	if err != nil {
		session := getSession(r)
		session.Values["notice"] = "画像が必須です"
		session.Save(r, w)

		http.Redirect(w, r, "/", http.StatusFound)
		return
	}

	mime := ""
	if file != nil {
		// 投稿のContent-Typeからファイルのタイプを決定する
		contentType := header.Header["Content-Type"][0]
		if strings.Contains(contentType, "jpeg") {
			mime = "image/jpeg"
		} else if strings.Contains(contentType, "png") {
			mime = "image/png"
		} else if strings.Contains(contentType, "gif") {
			mime = "image/gif"
		} else {
			session := getSession(r)
			session.Values["notice"] = "投稿できる画像形式はjpgとpngとgifだけです"
			session.Save(r, w)

			http.Redirect(w, r, "/", http.StatusFound)
			return
		}
	}

	filedata, err := io.ReadAll(file)
	if err != nil {
		log.Print(err)
		return
	}

	if len(filedata) > UploadLimit {
		session := getSession(r)
		session.Values["notice"] = "ファイルサイズが大きすぎます"
		session.Save(r, w)

		http.Redirect(w, r, "/", http.StatusFound)
		return
	}

	query := "INSERT INTO `posts` (`user_id`, `mime`, `imgdata`, `body`) VALUES (?,?,?,?)"
	result, err := db.Exec(
		query,
		me.ID,
		mime,
		[]byte{}, // filedata,
		r.FormValue("body"),
	)
	if err != nil {
		log.Print(err)
		return
	}

	pid, err := result.LastInsertId()
	if err != nil {
		log.Print(err)
		return
	}
	var ext string
	switch mime {
	case "image/jpeg":
		ext = "jpg"
	case "image/png":
		ext = "png"
	case "image/gif":
		ext = "gif"
	}
	writeImageToFile(filedata, fmt.Sprintf("%d.%s", pid, ext))

	http.Redirect(w, r, "/posts/"+strconv.FormatInt(pid, 10), http.StatusFound)
}

func writeImageToFile(image []byte, name string) error {
	f, err := os.Create(fmt.Sprintf("/home/isucon/private_isu/webapp/img/%s", name))
	if err != nil {
		return fmt.Errorf("failed to create image file %s: %w", name, err)
	}
	defer f.Close()
	_, err = f.Write(image)
	if err != nil {
		return fmt.Errorf("failed to write image file %s: %w", name, err)
	}
	return nil
}

func getImage(w http.ResponseWriter, r *http.Request) {
	pidStr := r.PathValue("id")
	pid, err := strconv.Atoi(pidStr)
	if err != nil {
		w.WriteHeader(http.StatusNotFound)
		return
	}

	ext := r.PathValue("ext")
	filename := fmt.Sprintf("/home/isucon/private_isu/webapp/img/%d.%s", pid, ext)
	_, err = os.Stat(filename)
	if err == nil {
		w.Header().Set("X-Accel-Redirect", filename)
		w.WriteHeader(http.StatusNoContent)
		return
	}

	post, ok := getPostFromCache(pid)
	if ok != nil {
		log.Print(err)
		return
	}

	if ext == "jpg" && post.Mime == "image/jpeg" ||
		ext == "png" && post.Mime == "image/png" ||
		ext == "gif" && post.Mime == "image/gif" {
		w.Header().Set("Content-Type", post.Mime)
		writeImageToFile(post.Imgdata, fmt.Sprintf("%d.%s", post.ID, ext))
		_, err := w.Write(post.Imgdata)
		if err != nil {
			log.Print(err)
			return
		}
		return
	}

	w.WriteHeader(http.StatusNotFound)
}

func getUser(userID int) (User, error) {
	userCached, ok := userCache.Load(userID)
	var user User
	if ok {
		user = userCached.(User)
	} else {
		err := db.Get(&user, "SELECT * FROM `users` WHERE `id` = ?", userID)
		if err != nil {
			return User{}, err
		}
		userCache.Store(userID, user)
	}
	return user, nil
}

func getPostFromCache(postID int) (Post, error) {
	postCached, ok := postCache.Load(postID)
	var post Post
	if ok {
		post = postCached.(Post)
	} else {
		err := db.Get(&post, "SELECT * FROM `posts` WHERE `id` = ?", postID)
		if err != nil {
			return Post{}, err
		}
		postCache.Store(postID, post)
	}
	return post, nil
}

func postComment(w http.ResponseWriter, r *http.Request) {
	me := getSessionUser(r)
	if !isLogin(me) {
		http.Redirect(w, r, "/login", http.StatusFound)
		return
	}

	if r.FormValue("csrf_token") != getCSRFToken(r) {
		w.WriteHeader(http.StatusUnprocessableEntity)
		return
	}

	postID, err := strconv.Atoi(r.FormValue("post_id"))
	if err != nil {
		log.Print("post_idは整数のみです")
		return
	}

	query := "INSERT INTO `comments` (`post_id`, `user_id`, `comment`) VALUES (?,?,?)"
	_, err = db.Exec(query, postID, me.ID, r.FormValue("comment"))
	if err != nil {
		log.Print(err)
		return
	}

	http.Redirect(w, r, fmt.Sprintf("/posts/%d", postID), http.StatusFound)
}

